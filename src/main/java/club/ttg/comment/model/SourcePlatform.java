package club.ttg.comment.model;

/**
 * Платформа-источник обсуждения. Набор значений общий с сервисом баг-репортов
 * (club.ttg.bug.report.model.SourcePlatform): пользователь один и тот же, платформы те же,
 * и расхождение в названиях заставило бы сопоставлять их руками в каждой админке.
 * <p>
 * Перечисление, а не свободная строка: опечатка в значении иначе завела бы отдельное
 * обсуждение с ключом, к которому никто больше не обратится, — и заметить это можно было бы
 * только по опустевшей ленте. Неизвестное значение отбивается на входе (400).
 */
public enum SourcePlatform
{
    /** Сайт 2024 — core-app. */
    SITE_5E24,

    /** Сайт 2014 — предыдущая версия сайта. */
    SITE_5E14,

    /** VTTG. */
    VTTG
}
