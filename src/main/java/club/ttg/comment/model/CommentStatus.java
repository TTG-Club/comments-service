package club.ttg.comment.model;

public enum CommentStatus
{
    PUBLISHED,

    /**
     * Мягко удалён автором или модератором. В отличие от остальных скрывающих статусов
     * не обрывает ветку: если под удалённым узлом остались опубликованные ответы
     * ({@code totalReplyCount > 0}), он отдаётся в публичных выдачах надгробием — без текста
     * и автора, но с местом в дереве, — а ответы продолжают жить. Без живых потомков узел
     * из выдач уходит; текст в любом статусе остаётся в базе и виден модератору.
     */
    DELETED,

    PENDING_MODERATION,
    REJECTED,
    SPAM,

    /**
     * Скрыт из-за блокировки автора в auth-service. Отдельный статус, а не переиспользование
     * существующих, потому что разблокировка должна вернуть ровно то, что скрыл бан:
     * <ul>
     *   <li>DELETED — комментарий удалил сам пользователь, и разбан не должен его воскрешать;
     *       по общему статусу эти два случая было бы не различить;</li>
     *   <li>REJECTED и SPAM — решения модератора по конкретному комментарию, снимать их
     *       при разблокировке автора нельзя.</li>
     * </ul>
     * Ветку, как и DELETED, не обрывает: если под скрытым узлом остались опубликованные ответы,
     * он отдаётся надгробием (см. {@link club.ttg.comment.model.Comment#isTombstoneStatus()}) —
     * чужие ответы под комментарием забаненного могут быть ценны сами по себе, и бан одного
     * автора не должен уносить их с собой. Наружу такое надгробие уходит под статусом DELETED:
     * читателю не сообщают, что автора заблокировали, а различать два вида надгробий ему незачем.
     * <p>
     * Текст, как и при любом другом статусе, остаётся в базе — сервис не затирает содержимое
     * ни в одном сценарии.
     */
    HIDDEN_BY_BAN
}